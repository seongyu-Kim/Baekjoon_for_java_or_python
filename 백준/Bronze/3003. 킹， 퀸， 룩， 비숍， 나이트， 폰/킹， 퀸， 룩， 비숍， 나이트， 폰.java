import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int k = 1, q = 1, l = 2, b = 2, n = 2, p = 8;

	
		k = k - sc.nextInt();
		q = q - sc.nextInt();
		l = l - sc.nextInt();
		b = b - sc.nextInt();
		n = n - sc.nextInt();
		p = p - sc.nextInt();
		
		System.out.print(k + " ");
		System.out.print(q + " ");
		System.out.print(l + " ");
		System.out.print(b + " ");
		System.out.print(n + " ");
		System.out.print(p + " ");
		
	}

}
